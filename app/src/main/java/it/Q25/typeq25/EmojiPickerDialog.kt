                // Helper function to translate category keys
                @Composable
                fun getCategoryName(categoryKey: String): String {
                    return when (categoryKey) {
                        "smileys" -> stringResource(R.string.emoji_category_smileys)
                        "animals" -> stringResource(R.string.emoji_category_animals)
                        "food" -> stringResource(R.string.emoji_category_food)
                        "travel" -> stringResource(R.string.emoji_category_travel)
                        "activities" -> stringResource(R.string.emoji_category_activities)
                        "objects" -> stringResource(R.string.emoji_category_objects)
                        "symbols" -> stringResource(R.string.emoji_category_symbols)
                        "flags" -> stringResource(R.string.emoji_category_flags)
                        else -> categoryKey
                    }
                }
                
                // Category tabs
                var selectedCategory by remember { mutableStateOf(emojiCategories.keys.first()) }
                
                // Tab selector (using scrollable Row instead of ScrollableTabRow for compatibility)
                Row(
                    modifier = Modifier
                        .fillMaxWidth()
                        .horizontalScroll(rememberScrollState()),
                    horizontalArrangement = Arrangement.spacedBy(4.dp)
                ) {
                    emojiCategories.keys.forEach { category ->
                        FilterChip(
                            selected = selectedCategory == category,
                            onClick = { selectedCategory = category },
                            label = { 
                                Text(
                                    getCategoryName(category), 
                                    style = MaterialTheme.typography.labelMedium // 20% larger than labelSmall
                                ) 
                            },
                            modifier = Modifier.padding(horizontal = 2.dp)
                        )
                    }
                }
                
                Spacer(modifier = Modifier.height(4.dp))
                
                // Emoji grid with RecyclerView for optimal performance
                val selectedEmojis = emojiCategories[selectedCategory] ?: emptyList()
                
                key(selectedCategory) {
                    Box(
                        modifier = Modifier
                            .fillMaxWidth()
                            .weight(1f)
                            .clipToBounds()
                    ) {
                        AndroidView(
                            factory = { context ->
                                val recyclerView = RecyclerView(context)
                                val screenWidth = context.resources.displayMetrics.widthPixels
                                val emojiSize = (48 * context.resources.displayMetrics.density).toInt() // 40 * 1.2 (20% larger)
                                val spacing = (2 * context.resources.displayMetrics.density).toInt()
                                val padding = (4 * context.resources.displayMetrics.density).toInt()
                                
                                // Calculate number of columns based on screen width
                                val columns = (screenWidth / (emojiSize + spacing)).coerceAtLeast(4)
                                
                                recyclerView.apply {
                                    layoutManager = GridLayoutManager(context, columns)
                                    adapter = EmojiRecyclerViewAdapter(selectedEmojis) { emoji ->
                                        onEmojiSelected(emoji)
                                        onDismiss()
                                    }
                                    setPadding(padding, padding, padding, padding)
                                    clipToPadding = false
                                    // Performance optimizations
                                    setHasFixedSize(true)
                                    setItemViewCacheSize(20)
                                }
                                recyclerView
                            },
                            modifier = Modifier.fillMaxSize()
                        )
                    }
                }
            }
